package ru.krasnopolsky.service.model;

public class AthletePerformance {

    private String name;
    private int totalScore;
    private Double sprint100;
    private Double longJump;
    private Double shotPut;
    private Double highJump;
    private Double sprint400;
    private Double sprintHurdles;
    private Double discusThrow;
    private Double poleVault;
    private Double javelinThrow;
    private Double sprint1500;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTotalScore() {
        return totalScore;
    }

    public void setTotalScore(int totalScore) {
        this.totalScore = totalScore;
    }

    public Double getSprint100() {
        return sprint100;
    }

    public void setSprint100(Double sprint100) {
        this.sprint100 = sprint100;
    }

    public Double getLongJump() {
        return longJump;
    }

    public void setLongJump(Double longJump) {
        this.longJump = longJump;
    }

    public Double getShotPut() {
        return shotPut;
    }

    public void setShotPut(Double shotPut) {
        this.shotPut = shotPut;
    }

    public Double getHighJump() {
        return highJump;
    }

    public void setHighJump(Double highJump) {
        this.highJump = highJump;
    }

    public Double getSprint400() {
        return sprint400;
    }

    public void setSprint400(Double sprint400) {
        this.sprint400 = sprint400;
    }

    public Double getSprintHurdles() {
        return sprintHurdles;
    }

    public void setSprintHurdles(Double sprintHurdles) {
        this.sprintHurdles = sprintHurdles;
    }

    public Double getDiscusThrow() {
        return discusThrow;
    }

    public void setDiscusThrow(Double discusThrow) {
        this.discusThrow = discusThrow;
    }

    public Double getPoleVault() {
        return poleVault;
    }

    public void setPoleVault(Double poleVault) {
        this.poleVault = poleVault;
    }

    public Double getJavelinThrow() {
        return javelinThrow;
    }

    public void setJavelinThrow(Double javelinThrow) {
        this.javelinThrow = javelinThrow;
    }

    public Double getSprint1500() {
        return sprint1500;
    }

    public void setSprint1500(Double sprint1500) {
        this.sprint1500 = sprint1500;
    }
}
